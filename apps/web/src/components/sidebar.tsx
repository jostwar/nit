const adminItems = [
  { key: "adminUsers", href: "/admin/users", label: "Usuarios" },
  { key: "adminFilters", href: "/admin/filters", label: "Filtros de Apps" },
  { key: "adminCatalog", href: "/admin/catalog", label: "Catálogo referencias" },
  { key: "adminReports", href: "/admin/reports", label: "Reportes de Ventas" },
];

const LOGO_SRC = (typeof process !== "undefined" && process.env.NEXT_PUBLIC_LOGO) || "/logo.png";

const DEFAULT_FILTERS: Record<string, boolean> = {
  dashboard: true,
  customers: true,
  alerts: true,
  ai: true,
  dataUpdate: true,
  adminUsers: true,
  adminFilters: true,
  adminCatalog: true,
  adminReports: true,
};

export function Sidebar() {
  const searchParams = useSearchParams();
  const pathname = usePathname();
  const [filters, setFilters] = useState<Record<string, boolean>>(DEFAULT_FILTERS);
  const queryString = searchParams.toString();

  useEffect(() => {
    const raw = window.localStorage.getItem("appFilters");
    if (!raw) return;
    try {
      const parsed = JSON.parse(raw) as Record<string, boolean>;
      setFilters({ ...DEFAULT_FILTERS, ...parsed });
    } catch {
      setFilters(DEFAULT_FILTERS);
    }
  }, []);

  const visibleNav = navItems.filter((item) => filters[item.key] !== false);
  const visibleAdmin = adminItems.filter((item) => filters[item.key] !== false);
  const hrefWithParams = (path: string) =>
    queryString ? `${path}?${queryString}` : path;

  const isActive = (href: string) => pathname === href || pathname.startsWith(href + "/");

  const linkClass = (href: string) =>
    `flex items-center gap-3 rounded-lg px-3 py-2 text-[13px] font-medium transition ${
      isActive(href)
        ? "bg-white/15 text-white"
        : "text-slate-300 hover:bg-white/10 hover:text-white"
    }`;

  return (
    <aside className="flex h-screen w-56 flex-shrink-0 flex-col border-r border-slate-700/50 bg-[var(--sidebar-bg)] px-3 py-5 lg:w-60">
      <Link href={hrefWithParams("/dashboard")} className="mb-6 flex items-center gap-2 px-3">
        <Image src={LOGO_SRC} alt="NITIQ" width={110} height={32} priority className="h-8 w-auto object-contain brightness-0 invert" />
      </Link>

      <nav className="flex flex-col gap-0.5">
        {visibleNav.map((item) => {
          const Icon = ICONS[item.key];
          return (
            <Link key={item.href} href={hrefWithParams(item.href)} className={linkClass(item.href)}>
              {Icon && <Icon />}
              {item.label}
            </Link>
          );
        })}
      </nav>

      <div className="mx-3 mt-6 mb-3 border-t border-slate-600/50" />
      <p className="mb-2 px-3 text-[10px] font-semibold uppercase tracking-widest text-slate-500">
        Administración
      </p>
      <nav className="flex flex-col gap-0.5">
        {visibleAdmin.map((item) => {
          const Icon = ICONS[item.key];
          return (
            <Link key={item.href} href={hrefWithParams(item.href)} className={linkClass(item.href)}>
              {Icon && <Icon />}
              {item.label}
            </Link>
          );
        })}
      </nav>

      <div className="mt-auto px-3 pt-4 text-center">
        <p className="text-[10px] font-medium text-slate-400">BPI - Soluciones Empresariales</p>
        <p className="text-[9px] text-slate-500">Powered By iPeakAgency</p>
      </div>
    </aside>
  );
}
